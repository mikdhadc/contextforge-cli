describe('runAllChecks', () => {
  let tmpDir: string;
  beforeEach(() => { tmpDir = makeTmp(); });
  afterEach(() => {
    delete process.env.CONTEXTFORGE_IDE;
    fs.rmSync(tmpDir, { recursive: true, force: true });
  });

  it('returns 6 checks', () => {
    const checks = runAllChecks(tmpDir);
    expect(checks).toHaveLength(6);
  });

  it('check IDs are all unique', () => {
    const checks = runAllChecks(tmpDir);
    const ids = checks.map(c => c.id);
    expect(new Set(ids).size).toBe(ids.length);
  });

  it('all checks fail/warn for an empty dir', () => {
    delete process.env.CONTEXTFORGE_IDE;
    const checks = runAllChecks(tmpDir);
    const passing = checks.filter(c => c.status === 'pass');
    expect(passing).toHaveLength(0);
  });
});

describe('runAudit --json', () => {
  let tmpDir: string;
  let stdout: string[];

  beforeEach(() => {
    tmpDir = makeTmp();
    stdout = [];
    vi.spyOn(console, 'log').mockImplementation((msg: string) => { stdout.push(msg); });
    vi.spyOn(process, 'exit').mockImplementation((() => {}) as () => never);
  });

  afterEach(() => {
    delete process.env.CONTEXTFORGE_IDE;
    vi.restoreAllMocks();
    fs.rmSync(tmpDir, { recursive: true, force: true });
  });

  it('outputs valid JSON with expected shape', async () => {
    await runAudit({ root: tmpDir, json: true });
    const output = stdout.join('');
    const report = JSON.parse(output);
    expect(report).toHaveProperty('projectRoot');
    expect(report).toHaveProperty('timestamp');
    expect(report).toHaveProperty('checks');
    expect(report).toHaveProperty('passed');
    expect(report).toHaveProperty('warned');
    expect(report).toHaveProperty('failed');
    expect(Array.isArray(report.checks)).toBe(true);
  });

  it('JSON checks array has 6 entries', async () => {
    await runAudit({ root: tmpDir, json: true });
    const report = JSON.parse(stdout.join(''));
    expect(report.checks).toHaveLength(6);
  });

  it('each check in JSON has id, status, detail, fixable', async () => {
    await runAudit({ root: tmpDir, json: true });
    const report = JSON.parse(stdout.join(''));
    for (const check of report.checks) {
      expect(check).toHaveProperty('id');
      expect(check).toHaveProperty('status');
      expect(check).toHaveProperty('detail');
      expect(check).toHaveProperty('fixable');
      expect(['pass', 'warn', 'fail']).toContain(check.status);
    }
  });

  it('failed count equals number of fail-status checks', async () => {
    await runAudit({ root: tmpDir, json: true });
    const report = JSON.parse(stdout.join(''));
    const failCount = report.checks.filter((c: { status: string }) => c.status === 'fail').length;
    expect(report.failed).toBe(failCount);
  });
});
