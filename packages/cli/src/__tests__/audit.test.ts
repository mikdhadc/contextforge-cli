describe('checkNoContestedConventions', () => {
  let tmpDir: string;
  beforeEach(() => { tmpDir = makeTmp(); });
  afterEach(() => { fs.rmSync(tmpDir, { recursive: true, force: true }); });

  it('fails when .context.md is missing', () => {
    const check = checkNoContestedConventions(tmpDir);
    expect(check.status).toBe('fail');
  });

  it('passes when no ⚠ Contested markers present', () => {
    fs.writeFileSync(path.join(tmpDir, '.context.md'), makeContextMd());
    const check = checkNoContestedConventions(tmpDir);
    expect(check.status).toBe('pass');
  });

  it('warns when contested conventions are present', () => {
    const content = makeContextMd() + '\n\n⚠ Contested: camelCase vs snake_case (60% vs 40%)\n⚠ Contested: import style';
    fs.writeFileSync(path.join(tmpDir, '.context.md'), content);
    const check = checkNoContestedConventions(tmpDir);
    expect(check.status).toBe('warn');
    expect(check.fixable).toBe(false);
    expect(check.detail).toContain('2');
  });
});

