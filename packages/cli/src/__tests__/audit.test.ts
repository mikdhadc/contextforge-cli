import { describe, it, expect, beforeEach, afterEach, vi } from 'vitest';
import * as fs from 'node:fs';
import * as path from 'node:path';
import * as os from 'node:os';
import { runAudit } from '../commands/audit.js';
import {
  checkContextExists,
  checkContextFresh,
  checkIdFilePresent,
  checkSectionsComplete,
  checkNoContestedConventions,
  checkTemplatesApplied,
  runAllChecks,
} from '../audit/checks.js';

function makeTmp(): string {
  return fs.mkdtempSync(path.join(os.tmpdir(), 'cf-audit-test-'));
}

// Minimal .context.md with all 6 sections and a stale hash
function makeContextMd(sections: string[] = ['stack', 'structure', 'conventions', 'dependencies', 'anti-patterns', 'decisions']): string {
  const header = '# Project Context\n\n> Generated by ContextForge\n> Last updated: 2024-01-01T00:00:00.000Z\n> Signature hash: 000000deadbeef\n\n';
  const body = sections.map(name =>
    `<!-- contextforge:${name}:start hash="abc" -->\n## ${name}\ncontent\n<!-- contextforge:${name}:end -->`,
  ).join('\n\n');
  return header + body;
}

describe('checkContextExists', () => {
  let tmpDir: string;
  beforeEach(() => { tmpDir = makeTmp(); });
  afterEach(() => { fs.rmSync(tmpDir, { recursive: true, force: true }); });

  it('fails when .context.md is missing', () => {
    const check = checkContextExists(tmpDir);
    expect(check.status).toBe('fail');
    expect(check.fixable).toBe(true);
  });

  it('passes when .context.md exists', () => {
    fs.writeFileSync(path.join(tmpDir, '.context.md'), '# Project Context\n');
    const check = checkContextExists(tmpDir);
    expect(check.status).toBe('pass');
  });
});

describe('checkContextFresh', () => {
  let tmpDir: string;
  beforeEach(() => { tmpDir = makeTmp(); });
  afterEach(() => { fs.rmSync(tmpDir, { recursive: true, force: true }); });

  it('fails when .context.md is missing', () => {
    const check = checkContextFresh(tmpDir);
    expect(check.status).toBe('fail');
  });

  it('warns when signature hash is stale', () => {
    fs.writeFileSync(path.join(tmpDir, '.context.md'), makeContextMd());
    const check = checkContextFresh(tmpDir);
    expect(check.status).toBe('warn');
    expect(check.fixable).toBe(true);
    expect(check.detail).toContain('stale');
  });
});

